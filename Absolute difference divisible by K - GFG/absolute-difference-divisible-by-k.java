class Solution {
	static long countPairs(int n, int[] arr, int k) {
		// code here
		long ans = 0l ;
		HashMap<Integer,Long> hm = new HashMap<>();
		for(int x : arr){
		    int rem = x % k ;
		    if(hm.containsKey(rem)){
		        ans += hm.get(rem);
		    }
		    hm.put(rem , hm.getOrDefault(rem,0l)+1) ;
		}
		return ans ;
	}
}